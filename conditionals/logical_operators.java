package com.dev.conditionals;

public class logical_operators {
    public static void main(String[] args) {

//        LOGICAL OPERATOR (AND OPERATOR)
        boolean a = true;
        boolean b = false;
        if(a&&b){
            System.out.println("Y");
        }else{
            System.out.println("N");
        }

//      LOGICAL OPERATOR (OR OPERATOR)
        if (a||b){
            System.out.println("y");
        }else {
            System.out.println("n");
        }
    }
}